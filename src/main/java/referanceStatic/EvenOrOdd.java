package referanceStatic;

public class EvenOrOdd {

    static void evenOrOdd (int number) {
        if(number % 2 ==0) {
            System.out.println(number + " is even");
        } else {
            System.out.println(number + " is odd");
        }
    }

    public void greet (int i) {
        System.out.println("Hello " + i);
    }

}
